package serealizacion;

public class ConfigPuntos {

    private int puntosPartido;
    private int puntosRonda;
    private  int puntosfase;

    public ConfigPuntos() {
    }

    public int getPuntosPartido() {
        return puntosPartido;
    }

    public void setPuntosPartido(int puntosPartido) {
        this.puntosPartido = puntosPartido;
    }

    public int getPuntosRonda() {
        return puntosRonda;
    }

    public void setPuntosRonda(int puntosRonda) {
        this.puntosRonda = puntosRonda;
    }

    public int getPuntosfase() {
        return puntosfase;
    }

    public void setPuntosfase(int puntosfase) {
        this.puntosfase = puntosfase;
    }
}
